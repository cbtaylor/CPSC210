package ca.ubc.cpsc210.alarm.model.exception;

import ca.ubc.cpsc210.alarm.model.AlarmCode;

/**
 * Represents the exception that occurs when an attempt is made to register
 * a code with the system and the code is not valid.
 */
public class NotValidCodeException extends AlarmException {
	public NotValidCodeException() {
		super();
	}
	
	public NotValidCodeException(AlarmCode code) {
		super("Code " + code + " is not valid.");
	}
}
