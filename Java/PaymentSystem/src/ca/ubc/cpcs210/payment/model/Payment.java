package ca.ubc.cpcs210.payment.model;

/**
 * A payment made by a customer.
 * 
 * @author Gail Murphy
 */
public interface Payment {

	/**
	 * Process a payment of a specified amount.
	 * 
	 * REQUIRES: amount > 0.0 
	 * MODIFIES: nothing 
	 * EFFECTS: returns a record of the payment which either records a successful 
	 * payment or indicates an error
	 */
	public PaymentRecord processPayment(double amount);

}
